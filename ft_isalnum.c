int ft_isalpha (int c)
{
    if (c >= 'A' && c <= 'Z')
        return (1);
    if (c >= 'a' && c <= 'z')
        return (1);
    if (c >= '0' && c <= '9')
        return (1);
    return (0);
}
#include <stdio.h>
int main (void)
{
    printf("%d", ft_isalpha('v'));
}